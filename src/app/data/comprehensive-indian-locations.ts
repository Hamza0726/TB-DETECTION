// Helper function to get all state names
export const getAllStateNames = (): string[] => {
  return allIndianStates.map(s => s.name).sort();
};

// Helper function to get districts by state
export const getDistrictsByStateName = (stateName: string): string[] => {
  const state = allIndianStates.find(s => s.name === stateName);
  if (!state) return [];
  return state.districts.map(d => d.name).sort();
};

// Helper function to search states by prefix
export const searchStatesByPrefix = (prefix: string): string[] => {
  const normalizedPrefix = prefix.toLowerCase().trim();
  if (!normalizedPrefix) return getAllStateNames();
  
  return allIndianStates
    .filter(state => state.name.toLowerCase().startsWith(normalizedPrefix))
    .map(s => s.name)
    .sort();
};

// Helper function to search districts by prefix (within a state)
export const searchDistrictsByPrefix = (stateName: string, prefix: string): string[] => {
  const state = allIndianStates.find(s => s.name === stateName);
  if (!state) return [];
  
  const normalizedPrefix = prefix.toLowerCase().trim();
  if (!normalizedPrefix) return state.districts.map(d => d.name).sort();
  
  return state.districts
    .filter(district => district.name.toLowerCase().startsWith(normalizedPrefix))
    .map(d => d.name)
    .sort();
};
